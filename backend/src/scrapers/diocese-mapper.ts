/**
 * 주소 문자열에서 교구를 추론합니다.
 * @param location - 행사 장소 주소 문자열
 * @returns 교구 이름 또는 null (추론 불가 시)
 */
export function inferDiocese(location: string | null | undefined): Diocese | null {
  if (!location) return null;

  // 이미 교구 이름이 포함된 경우 바로 반환
  for (const diocese of DIOCESES) {
    if (location.includes(diocese.replace('교구', '').replace('대', ''))) {
      // "서울대교구" → "서울" 포함 여부 체크
    }
  }

  // 키워드 매핑으로 교구 추론
  for (const [keywords, diocese] of KEYWORD_MAP) {
    for (const kw of keywords) {
      if (location.includes(kw)) {
        return diocese;
      }
    }
  }

  return null;
}
